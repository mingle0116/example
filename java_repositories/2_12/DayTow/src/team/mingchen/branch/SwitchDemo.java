package team.mingchen.branch;

public class SwitchDemo {
    public static void main(String[] args) {
        String weekday="周三";
        switch(weekday){
            case "周一":
                System.out.println("zhouyi");
                break;
            case "周二":
                System.out.println("zhouyi2");
                break;
            case "周三":
                System.out.println("zhouyi3");
                break;
        }
    }
}
