package team.mingchen.branch;

public class ForDemo {
    public static void main(String[] args) {
        for(int i=0;i<3;i++){
            System.out.println("hello world");
        }
        System.out.println("--------------");
        for(int i=0;i<5;i+=2){
            System.out.println("hello world");
        }
    }

}
